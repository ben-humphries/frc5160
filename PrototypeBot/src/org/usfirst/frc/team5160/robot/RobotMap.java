package org.usfirst.frc.team5160.robot;
/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	//drive motors
	public static int FRONT_LEFT_CIM = 0;
	public static int BACK_LEFT_CIM = 1;
	public static int FRONT_RIGHT_CIM = 2;
	public static int BACK_RIGHT_CIM = 3;
	
	
	//joysticks
	public static int JOYSTICK = 0;
	public static int TANK_JOYSTICK = 1;
	public static int OPERATOR_JOYSTICK = 2;
}